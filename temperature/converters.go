// Package temperature contains the implementation of conversion to Celsius, Fahrenheit and Kelvin
package temperature

// CelsiusToFahrenheit - converts Celsius to Fahrenheit
func CelsiusToFahrenheit(data int) int {
	return (data * 9 / 5) + 32
}

